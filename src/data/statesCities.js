export const stateOptions = statesCities.map((entry) => entry.name);

const stateLookup = statesCities.reduce((acc, entry) => {
  acc.set(entry.name.toLowerCase(), entry);
  return acc;
}, new Map());

export const getStateEntry = (name) => {
  if (!name) {
    return null;
  }
  return stateLookup.get(name.trim().toLowerCase()) || null;
};

export const getCanonicalStateName = (name) => {
  if (!name) {
    return '';
  }
  const match = getStateEntry(name);
  return match ? match.name : name.trim();
};

export const getCitiesByState = (name) => {
  const match = getStateEntry(name);
  if (!match) {
    return [];
  }
  return match.cities;
};